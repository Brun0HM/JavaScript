//Operadores de incremento e decremento
var idade = 16;
console.log(idade);

idade++; //idade = idade +1
console.log(idade);

idade--; //idade = idade -1
console.log(idade);

console.log("---------------");

var novaIdade = idade++;
console.log("idade:", idade, "| Nova idade:", novaIdade);

var novaIdade = ++idade;
console.log("idade:", idade, "| Nova idade:", novaIdade);

//incremento e decremento com valor específico
//Operadores de atribuição aritimética
novaIdade += 10;
console.log(novaIdade);

novaIdade -=20
console.log(novaIdade)
