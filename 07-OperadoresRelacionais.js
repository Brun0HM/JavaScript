//Operadores relacionais
var x = 22;
var y = 5;
console.log("-------------------------------");
console.log("x =", x, "| y =", y);

console.log("x é igual a y? |", x == y); // comparação
console.log("x é diferente a y? |", x != y); // != é diferente
console.log("x é maior que y? |", x > y) // >maior
console.log("x é menor que y? |", x < y) // <menor
console.log("x é maior ou igual que y? |", x >= y) // <maior igual
console.log("x é maior que ou igual y? |", x <= y) // >menor igual

